# String concatenation
first_name = "Eswari"
last_name = "Jeyaram"
full_name = first_name + " " + last_name
print(f"Full Name: {full_name}")

#string length
full_name_len = len(full_name)
print(f"Length of full name: {full_name_len}")
print("Length :", full_name_len)

#splitting a string
name_parts = full_name.split()
print(f"Name Parts: {name_parts}")

